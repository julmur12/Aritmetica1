package Clases;

public class ClasePrincipal {
	
	public ClasePrincipal() {
	}

	public int suma(int num1, int num2) {
		int total;
		total = num1+num2;
		return total;
	}

	public int resta(int num1, int num2) {
		int total;
		total = num1-num2;
		return total;
	}
	
	public int mult(int num1, int num2) {
		int total;
		total = num1*num2;
		return total;
	}
	
	public float division(int num1, int num2) {
		int total;
		total = num1/num2;
		return total;
	}
	
}
